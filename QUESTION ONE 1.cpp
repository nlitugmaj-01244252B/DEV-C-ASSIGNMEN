#include <iostream>
using namespace std;

int main() 

{
	int x1,x2,sum,difference,product,quotient;
	
	cout<<"Enter x1: ";
	cin>>x1;
	cout<<"Enter x2: ";
	cin>>x2;
	
	sum=x1+x2;
	cout<<"sum of two numbers: "
	<<sum;
	

	difference=x1-x2;
	cout<<"difference of two numbers: "
	<< difference;
	
	product=x1*x2;
	cout<<"product of two numbers: "
	<<product;
	
	quotient=x1/x2;
	cout<<"quotient of two numbers: "
	<<quotient;
	return 0;
	
}


